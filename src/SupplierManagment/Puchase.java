    private void txt_total_amountKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_total_amountKeyReleased
        // TODO add your handling code here:
        
        Pur_Am.setText("");
        
        String Total_Amount = txt_total_amount.getText();
        if(Total_Amount.matches("^\\d{0,8}(\\.\\d{1,4})?$")){
            
            txt_total_amount.setBackground(Color.green);
            
        } else {
            
            txt_total_amount.setBackground(new Color(255,102,102));
            Pur_Am.setText("Only Accept Numbers");
        }
        
    }//GEN-LAST:event_txt_total_amountKeyReleased

