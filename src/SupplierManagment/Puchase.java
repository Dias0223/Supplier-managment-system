    public void ProductCode() {
        
        
        String pcode = txt_Sup_Id.getText();
        
        try {
            pst =con.prepareStatement("select * from supplier where Sup_Id=?");
            pst.setString(1,pcode);
            rs = pst.executeQuery();
            
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"Supplier ID Not Found","Warning",JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                String Sup_Id=rs.getString("Sup_Id");
                String Sup_Name =rs.getString("Sup_Name");
                
                txt_Sup_Id.setText(Sup_Id.trim());
                txt_sup_name.setText(Sup_Name.trim());
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Puchase.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
    }
    
    
    
    
    
    
    
    
    public void load(){
        
        int a;
        
        try {
            pst=con.prepareStatement("select*from purchase");
            ResultSet rs=pst.executeQuery();
            
            ResultSetMetaData rd=rs.getMetaData();
            a = rd.getColumnCount();
            df =(DefaultTableModel)Purchase_Table.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                
                Vector v2 = new Vector();
                for(int i=1; i<=a; i++ ){
                    
                    v2.add(rs.getString("Pay_No"));
                    v2.add(rs.getString("Sup_Id"));
                    v2.add(rs.getString("Sup_Name"));
                    v2.add(rs.getString("Description"));
                    v2.add(rs.getString("Total_Amount"));
                    v2.add(rs.getString("Date_Time"));
                  
                    
                }
                df.addRow(v2);
            }
            
            
            
            
            
        } catch (SQLException ex) {
            Logger.getLogger(Puchase.class.getName()).log(Level.SEVERE, null, ex);
        }
     
     } 
     
     
     
    
    
    
    
    
    
    
    
    
    
    

    

