    private void txt_or_qtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_or_qtyKeyReleased
        // TODO add your handling code here:
        
        Or_Qty.setText("");
        
        String Quantity = txt_or_qty.getText();
        if(Quantity.matches("^[0-9]*$")){
            
            txt_or_qty.setBackground(Color.green);
            
        } else {
            
            txt_or_qty.setBackground(new Color(255,102,102));
            Or_Qty.setText("Only Accept Numbers");
        }
        
    }//GEN-LAST:event_txt_or_qtyKeyReleased

