    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        

            // TODO add your handling code here:
        if(txt_pro_id.getText().trim().isEmpty()){
            
            Or_Id.setText("Product ID is Empty");
            
        }else if(txt_sup_name.getText().trim().isEmpty()){
            
            Or_Name_Sup.setText("Supplier Name is Empty");
            
        }else if(txt_pro_name.getText().trim().isEmpty()){
            
            Or_Name_Pro.setText("Product Name is Empty");
            
        }else if(txt_or_qty.getText().trim().isEmpty()){
            
           Or_Qty.setText("Quantity is Empty");
            
        }else
            
            
            try {
            
                String Sup_Name =txt_sup_name.getText();
                String Pro_Name =txt_pro_name.getText();
                String Quantity =txt_or_qty.getText();
                
            
            
            
            // TODO add your handling code here:

            pst= con.prepareStatement("insert into sup_order(Sup_Name,Pro_Name,Quantity)values(?,?,?)");
            
            pst.setString(1, Sup_Name);
            pst.setString(2, Pro_Name);
            pst.setString(3, Quantity);
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"Order Information Add Successfully","Info",JOptionPane.INFORMATION_MESSAGE);
            
            txt_pro_id.setText("");
            txt_sup_name.setText("");
            txt_or_qty.setText("");
            txt_pro_name.setText("");
            
            txt_pro_id.requestFocus();
            load();
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Order Information Can Not Add","Error",JOptionPane.ERROR_MESSAGE);
        }

        

    }//GEN-LAST:event_btn_addActionPerformed

