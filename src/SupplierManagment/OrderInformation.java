    public void ProductCode() {
        
        
        String pcode = txt_pro_id.getText();
        
        try {
            pst =con.prepareStatement("select * from product where Pro_Code=?");
            pst.setString(1,pcode);
            rs = pst.executeQuery();
            
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"Product ID Not Found","Warning",JOptionPane.WARNING_MESSAGE);
            }
            else
            {
                String Pro_Name=rs.getString("Pro_Name");
                String Sup_Name=rs.getString("Sup_Name");
               
                
                txt_pro_name.setText(Pro_Name.trim());
                txt_sup_name.setText(Sup_Name.trim());
                txt_or_qty.requestFocus();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(OrderInformation.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    
    }
    
    
    
     public void load(){
        
        int a;
        
        try {
            pst=con.prepareStatement("select*from sup_order");
            ResultSet rs=pst.executeQuery();
            
            ResultSetMetaData rd=rs.getMetaData();
            a = rd.getColumnCount();
            df =(DefaultTableModel)Or_table.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                
                Vector v2 = new Vector();
                for(int i=1; i<=a; i++ ){
                    
                    v2.add(rs.getString("Order_Id"));
                    v2.add(rs.getString("Pro_Name"));
                    v2.add(rs.getString("Sup_Name"));
                    v2.add(rs.getString("Quantity"));
                    v2.add(rs.getString("Date_Time"));
                    
                    
                }
                df.addRow(v2);
            }
               
            
        } catch (SQLException ex) {
            Logger.getLogger(OrderInformation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
      
        
    }
    
    
    
    
    
